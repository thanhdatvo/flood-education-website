6.2. Khi lũ xảy ra

<br />
Di chuyển đến nơi cao, an toàn

<br />
Không cố đi qua nơi nước chảy mạnh

<br />
Gửi tín hiệu cầu cứu nếu bị cô lập

<br />
Tuyệt đối không bơi trong nước lũ

<br />
Giữ liên lạc với chính quyền địa phương

<br />
6.3. Sau khi lũ rút

<br />
