5 điều cần nhớ khi có lũ
<br />
1. Ưu tiên an toàn tính mạng

<br />
Nhanh chóng di chuyển lên nơi cao, an toàn.

<br />
Không cố gắng giữ tài sản khi nước lên nhanh.

<br />
2. Tuyệt đối không đi qua vùng nước chảy xiết

<br />
Dòng nước lũ có thể mạnh hơn bạn nghĩ.

<br />
Tránh đi lại bằng xe máy, ô tô qua vùng ngập sâu.

<br />
3. Ngắt điện, gas trước khi rời nhà

<br />
Tắt toàn bộ nguồn điện để tránh rò rỉ gây tai nạn.

<br />
Bảo vệ các thiết bị điện, giấy tờ quan trọng.

<br />
4. Chuẩn bị các vật dụng cần thiết

<br />
Nước uống sạch, thức ăn khô, đèn pin, áo mưa, pin dự phòng.

<br />
Thuốc men và vật dụng y tế cơ bản.

<br />
5. Cập nhật thông tin liên tục

<br />
Theo dõi cảnh báo từ chính quyền, lực lượng cứu hộ.

<br />
Không tin vào tin đồn thất thiệt.

<br />
Cách tự bảo vệ bản thân

<br />
1. Giữ bình tĩnh – đánh giá tình huống

<br />
Xác định mực nước, tốc độ nước chảy.

<br />
Quan sát lối thoát an toàn gần nhất.

<br />
2. Tránh xa các khu vực nguy hiểm

<br />
Không đến gần sông suối, cầu tràn, cống nước.

<br />
Không đứng trên mái tôn trơn trượt nếu không cần thiết.

<br />
3. Tuyệt đối không đi bộ hoặc lái xe qua nước sâu

<br />
Nước chỉ 30 cm cũng đủ cuốn trôi người và xe.

<br />
Nếu buộc phải di chuyển, dùng gậy dò đường.

<br />
4. Bảo vệ cơ thể

<br />
Mặc áo phao hoặc dùng vật nổi (can nhựa, thùng xốp).

<br />
Giữ ấm cơ thể, tránh nhiễm lạnh.

<br />
Đi giày kín hoặc ủng để tránh vật sắc nhọn dưới nước.

<br />
5. Tránh chạm vào điện

<br />
Không đến gần dây điện rơi, cột điện ngập nước.

<br />
Nếu nước vào nhà: lập tức ngắt cầu dao tổng.

<br />
6. Mang theo các vật dụng sinh tồn

<br />
Đèn pin, sạc dự phòng, nước uống, đồ ăn khô.

<br />
Giấy tờ quan trọng để trong túi chống nước.

<br />
7. Luôn đi cùng người khác

<br />
Không tách đoàn khi được sơ tán.

<br />
Hỗ trợ nhau vượt qua khu vực ngập.

<br />
8. Cập nhật thông tin liên tục

<br />
Nghe đài, loa xã, theo dõi thông báo từ lực lượng cứu hộ.

<br />
Không tin theo tin đồn hoặc “tự phán đoán”.

